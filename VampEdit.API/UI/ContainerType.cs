namespace VampEdit.API.UI
{
    /// <summary>
    /// The container type defines the container which gets loaded by the UI.
    /// </summary>
    public enum ContainerType
    {
        /// <summary>
        /// The sidebar of the main window.
        /// </summary>
        Sidebar,
        /// <summary>
        /// A container which holds the pages which should be toggled by the sidebar.
        /// </summary>
        Pages
    }
}
